<script>
$(document).ready(function() {
    $('#loginForm').submit(function(e) {
        e.preventDefault();
        var formData = {
            username: $('#username').val(),
            password: $('#password').val()
        };

        $.ajax({
            url: '<?= base_url("auth/doLogin") ?>',
            type: 'POST',
            data: formData,
            dataType: 'json',
            success: function(response) {
                if (response.status === 'success') {
                    $('#message').html('<p class="text-success">' + response.message +
                        '</p>');
                    window.location.href = '<?= base_url("/") ?>';
                } else {
                    $('#message').html('<p class="text-danger">' + response.message +
                        '</p>');
                }
            },
            error: function() {
                $('#message').html(
                    '<p class="text-danger">Terjadi kesalahan. Coba lagi nanti.</p>');
            }
        });
    });
});
</script>